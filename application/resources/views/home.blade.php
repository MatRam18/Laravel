            <h2>
                Características Visuais
            </h2>
            <h4>
                A estética é definida por cores metálicas, acabamentos brilhantes e tons neon, como prata e azul. 
                Predominam formas geométricas, texturas tecnológicas (circuitos, gráficos vetoriais) e a sensação 
                de um futuro altamente digitalizado.
            </h4>
            <br>
            <h2>
                Moda e Cultura Pop
            </h2>
            <h4>
                A moda Y2K mistura materiais sintéticos, como vinil e metal, e tem uma estética futurista, com roupas 
                brilhantes e acessórios de design inovador. Filmes como Matrix e Blade Runner influenciaram essa 
                moda e as representações de tecnologia.
            </h4>
            <br>
            <h2>
                Resgate Contemporâneo
            </h2>
